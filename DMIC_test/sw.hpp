#include <cstdint>

namespace utils {

    //+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++//
    /*!
        @brief  SW2 クラス
		@param[in]	B0	ビット０
		@param[in]	B1	ビット１
    */
    //+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++//
	template <class B0, class B1>
	class sw2 {
		uint8_t		lvl_;
		uint8_t		neg_;
		uint8_t		pos_;

	public:
        //-----------------------------------------------------------------//
        /*!
            @brief  コンストラクタ
        */
        //-----------------------------------------------------------------//
		sw2() : lvl_(0), neg_(0), pos_(0) { }


        //-----------------------------------------------------------------//
        /*!
            @brief  レベルの取得
			@return レベル
        */
        //-----------------------------------------------------------------//
		uint8_t level() const { return lvl_; }


        //-----------------------------------------------------------------//
        /*!
            @brief  押した瞬間の取得
			@return 押した瞬間
        */
        //-----------------------------------------------------------------//
		uint8_t positive() const { return pos_; }


        //-----------------------------------------------------------------//
        /*!
            @brief  離した瞬間の取得
			@return 離した瞬間
        */
        //-----------------------------------------------------------------//
		uint8_t negative() const { return neg_; }


        //-----------------------------------------------------------------//
        /*!
            @brief  開始
        */
        //-----------------------------------------------------------------//
		void start() {
			B0::DIR = 0;
			B1::DIR = 0;
		}


        //-----------------------------------------------------------------//
        /*!
            @brief  値の取得
			@return 値
        */
        //-----------------------------------------------------------------//
		uint8_t get() const {
			uint8_t l = static_cast<uint8_t>(B0::P()) | (static_cast<uint8_t>(B1::P()) << 1);
			return l ^ 0b11;
		}


        //-----------------------------------------------------------------//
        /*!
            @brief  サービス
        */
        //-----------------------------------------------------------------//
		void service()
		{
			uint8_t lvl = get();
			pos_ =  lvl & ~lvl_;
			neg_ = ~lvl &  lvl_;
			lvl_ = lvl;
		}

	};


    //+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++//
    /*!
        @brief  SW5 クラス
		@param[in]	B0	ビット０
		@param[in]	B1	ビット１
		@param[in]	B2	ビット２
		@param[in]	B3	ビット３
		@param[in]	B4	ビット４
    */
    //+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++//
	template <class B0, class B1, class B2, class B3, class B4>
	class sw5 {
	public:

        //-----------------------------------------------------------------//
        /*!
            @brief  開始
        */
        //-----------------------------------------------------------------//
		void start() {
			B0::DIR = 0;
			B1::DIR = 0;
			B2::DIR = 0;
			B3::DIR = 0;
			B4::DIR = 0;
		}


        //-----------------------------------------------------------------//
        /*!
            @brief  値の取得
			@return 値
        */
        //-----------------------------------------------------------------//
		uint8_t get() const {
			uint8_t v = static_cast<uint8_t>(B0::P())
					 | (static_cast<uint8_t>(B1::P()) << 1)
					 | (static_cast<uint8_t>(B2::P()) << 2)
					 | (static_cast<uint8_t>(B3::P()) << 3)
					 | (static_cast<uint8_t>(B4::P()) << 4);
			return v ^ 0b11111;
		}
	};
}
